def subtract(a, b):
    """
    Odejmowanie: zwraca wynik odejmowania liczby 'b' od liczby 'a'.
    Przykład:
        subtract(10, 3)  # zwróci 7
    """
    return a - b


def power(a, b):
    """
    Potęgowanie: podnosi liczbę 'a' do potęgi 'b' i zwraca wynik.
    Przykład:
        power(2, 3)  # zwróci 8
    """
    return a ** b
